        if not(isa(p.k0_pos,'function_handle')) p.k0_pos = @(T) p.k0_pos; end  
        p.k0 = @(T) [p.k0_neg(T)*ones(p.nn,1); p.k0_pos(T)*ones(p.np,1)];
