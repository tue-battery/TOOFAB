function out = DFN_Ageing_Cycle2(input_current,final_time,init_cond,varargin)
p = default_parameters();

if nargin>3
    p = process_param(p,varargin{1}); 
end

if length(init_cond)>1
    p.ageing = 1;
end
par_or = p;
time_max = final_time; 
if isa(input_current,'function_handle')
    input_mode = 2; 
    i_app = 0; 
elseif size(input_current,2)==2
    input_mode = 1; 
    F_current = griddedInterpolant(input_current(:,1),input_current(:,2),p.current_interp,p.current_extrap); 
    i_app = F_current(0); 
elseif isscalar(input_current)
    input_mode = 0;
    i_app = input_current;
else
    error('input_current not specified correctly, please check the documentation for the proper specification of input_current')
end
% warning('on')
warning('off','MATLAB:nearlySingularMatrix');
warning('off','MATLAB:illConditionedMatrix');

%% Define variables for simulation
if nargin>3
    p = fcn_system_vectors(p,init_cond,varargin{1});
else
    p = fcn_system_vectors(p,init_cond);
end

par_or.EMF = p.EMF; par_or.U_pos = p.U_pos_out; par_or.U_neg = p.U_neg_out;
par_or.dU_pos = p.dU_pos; par_or.dU_neg = p.dU_neg; 
par_or.cs_max_neg = p.cs_max_neg; par_or.cs_max_pos = p.cs_max_pos; 
m.dummy = 0; 
m = fcn_system_matrices(p,m);

[cs_prevt, ce_prevt, phis_prev,T_prevt,Cl_prevt,Rf] = init(p,m,init_cond);
max_prealloc_size = final_time;
phis = zeros(p.nnp,min(max_prealloc_size,round(time_max/p.dt)));
phie = zeros(p.nx,min(max_prealloc_size,round(time_max/p.dt)));
if p.set_simp(6)
    cs = zeros(p.np*2+p.nn*2,round(time_max/p.dt));
else
    zeros(p.np*p.nrp+p.nn*p.nrn,min(max_prealloc_size,round(time_max/p.dt)));
end
ce = zeros(p.nx,min(max_prealloc_size,round(time_max/p.dt)));
j_tot = zeros(p.nnp,min(max_prealloc_size,round(time_max/p.dt)));
eta_main = zeros(p.nnp,min(max_prealloc_size,round(time_max/p.dt)));
i0 = zeros(p.nnp,min(max_prealloc_size,round(time_max/p.dt)));
U = zeros(p.nnp,min(max_prealloc_size,round(time_max/p.dt)));
V = zeros(1,min(max_prealloc_size,round(time_max/p.dt)));
T = zeros(1,min(max_prealloc_size,round(time_max/p.dt)));
eta_sei = zeros(p.nnp,min(max_prealloc_size,round(time_max/p.dt)));
j_sei = zeros(p.nnp,min(max_prealloc_size,round(time_max/p.dt)));

% ~~~~~~~~~~~ Ageing Variables ~~~~~~~~~~~ %
epse_neg_temp = zeros(p.nnp,min(max_prealloc_size,round(time_max/p.dt)));
epse_neg_temp(:,1) = p.epse_neg;
j_lpl = zeros(p.nnp,min(max_prealloc_size,round(time_max/p.dt)));
j_cath = zeros(p.nnp,min(max_prealloc_size,round(time_max/p.dt)));
A_max = p.A_max; % 0.05; % Estimated from Given Particle size and Fig. 4 Barai et al.
A_cr = zeros(1,round(time_max/p.dt));
Ds_neg_crack = zeros(1,round(time_max/p.dt));
Ds_neg = p.Ds_neg;
epss_neg_temp = zeros(p.nn,min(max_prealloc_size,round(time_max/p.dt))); 
epss_neg_temp(:,1) = p.epss_neg;
sigma_neg = zeros(p.nn,min(max_prealloc_size,round(time_max/p.dt))); 
sigma_neg(:,1) = p.sigma_eff_neg';
a_s_neg = zeros(p.nn,min(max_prealloc_size,round(time_max/p.dt))); 
a_s_neg(:,1) = p.a_s_neg;
epss_pos_temp = zeros(p.nn,min(max_prealloc_size,round(time_max/p.dt))); 
epss_pos_temp(:,1) = p.epss_pos;
a_s_pos = zeros(p.np,min(max_prealloc_size,round(time_max/p.dt)));
a_s_pos(:,1) = p.a_s_pos;
sigma_pos = zeros(p.np,min(max_prealloc_size,round(time_max/p.dt)));
sigma_pos(:,1) = p.sigma_eff_pos';
if p.ageing
    se_init = [p.s100_neg; p.s100_pos; p.s0_neg; p.s0_pos]; 
    p.i02 = p.i02f(T_prevt); 
end
soc = compute_soc(cs_prevt,p); 
soc_prevt = soc; 
if soc <0 || soc>1+1e-3
    warning('Initial SOC = %d not in the range of [0,1]',soc)
end
if isa(p.k0,'function_handle')
    p.k0 = p.k0f(T_prevt); 
end
%- prev indicates the condition of the state at k-1

V(1) = phis_prev(end)-phis_prev(1);
T(1) = T_prevt; 
cs(:,1) = cs_prevt;
ce(:,1) = ce_prevt;
phis(:,1) = phis_prev;
Cl(1) = Cl_prevt;
Closs_LAM(1) = 0;
Closs_cath(1) = 0;
Closs_SEI(1) = 0;
Closs_LPL(1) = 0;
Rf_prevt = Rf; 
%Measure simulation time for benchmark purposes
if p.verbose>0
    dispstat('Starting simulation...','keepthis','timestamp')
end

mem.dummy = 0;

% ~~~~~~~~~~~ Memory Variables ~~~~~~~~~~~ %
mem.CycleNo = 1; % Initialize The Cycle NUmber for CC-CV Charging 
mem.TotCycle = p.TotCycle; % Set the total number of cycles for CC-CV Charging
mem.Closs(1) = 0;
mem.Cbat_aged(1) = 0;
mem.CycleTimeStamp(1) = 0;
mem.Cbat_aged_current = p.Cbat;

warning_set = 0;
t_vec = 0; 
end_simulation = 0; 
solution_time = 0;
num_iter = 0; 
max_iterations = 0;
dt_or = p.dt; 
%% Simulation
t= 1;
ic = 0; 
kl = 1;
kl2 = 0;
while not(end_simulation)
    %- Inner loop --------------------------------------------------------%
    tic()
    dt_prev = p.dt;
    if input_mode==2
        if nargout(input_current) == 4
            [i_app(t+1),mem,end_simulation,dt_user] = input_current(t,t_vec(t),i_app,V,soc,cs,ce,phis,phie,mem,p); 
            if dt_user>=dt_or
                dt_or = dt_user;
                p.dt = dt_or;
            end
            if dt_user<dt_or
                dt_or = dt_user;
                p.dt = dt_or;
               
           
            end
        elseif nargout(input_current)==3
            [i_app(t+1),mem,end_simulation] = input_current(t,t_vec(t),i_app,V,soc,cs,ce,phis,phie,mem,p);
        else
            error('input_current detected as a function handle, but does not have the required inputs and outputs defined.')
        end
    end
    
    if max_iterations %if max iterations were exceeded in the Newton's algorithm at the previous time step
        p.dt = dt_next; %decrease the time step
%     elseif not(max_iterations) && not(dt_or==p.dt) && kl2==0 
    else
        p.dt = dt_or-mod(t_vec(t),dt_or); %if max iterations were not exceeded in the Newton's algorithm at the previous step (i.e., algorithm converged), choose time step as the originally chosen time step
        if p.dt == 0
            p.dt = dt_or;
        end
    end
    
    if p.dt<1e-60 %if the time step is very small it means that the Newton's algorithm could not converge even after choosing a very small time step. 
        warning('Algorithm did not converge at time %d after lowering the sampling time. Most likely, the model is being pushed into an infeasible region (Either cs>1 or ce<0). Try changing the input or parameters. Stopping simulation',t_vec(end))
        end_simulation =1;
    end
    t_vec(t+1) = t_vec(t)+p.dt; 
    if input_mode==0
        i_app(t+1) = input_current;
    end
    if input_mode==1
        i_app(t+1) = F_current(t_vec(t+1));  
    end
    if(not(isequal(dt_prev,p.dt)))
        m = fcn_system_matrices(p,m);
    end
    
    m = fcn_system_matrices2(p,m,cs_prevt,ce_prevt); 
    
    if t_vec(t)==1823
        temp = 1;
    end
    % Solve set of AEs using Newton's method at time t
    for k=1:p.iter_max
        % Obtain function matrix and Jacobian       
        [F_phis,cs(:,t+1),ce(:,t+1),phie(:,t+1),j_tot(:,t+1),j_sei(:,t+1),j_lpl(:,t+1),j_cath(:,t+1),i0(:,t+1),eta_main(:,t+1),eta_sei(:,t+1),eta_lpl(:,t+1),eta_cath(:,t+1),U(:,t+1),p,m] = fcn_F(phis_prev,ce_prevt,cs_prevt,T_prevt,i_app(t+1),p,m);
        conv_check = norm(F_phis,2); 
        num_iter = num_iter+1; 
       % If algorithm doesn't converge, then display a warning
        if k==p.iter_max
            if not(max_iterations)
                ic = 0; 
            end
            warning_set = 1;
            max_iterations = 1;
            dt_inter = 0;
            ic = ic+1;
            end_simulation = 0;
            break
        end
        % If criterium for convergence is met, then break loop
        if(conv_check<p.tol ||end_simulation==1)
            max_iterations = 0;
            kl = 1;
            break
        end
        [J_phis,p] = fcn_J(cs(:,t+1),ce(:,t+1),j_tot(:,t+1),j_tot(:,t+1),i0(:,t+1),eta_main(:,t+1),eta_sei(:,t+1),eta_lpl(:,t+1),eta_cath(:,t+1),T_prevt,p,m); 
        
        if t==1
            phis_prev = real(phis_prev-(J_phis\F_phis)); 
        else
            phis_prev = real(phis_prev-(J_phis\F_phis)); 
        end
        
    end
    phis(:,t+1) = phis_prev; 
    % ~~~~~~~~~~~ Compute Terminal Voltage ~~~~~~~~~~~ %
    V(t+1) = phis(end,t+1)+i_app(t+1)/p.A_surf*p.dx(end)*0.5/p.sigma_eff(end)...
        -phis(1,t+1)-i_app(t+1)/p.A_surf*p.dx(1)*0.5/p.sigma_eff(1)...
        +(p.R_cc/p.A_surf)*i_app(t+1);
    if mod(t,100)==0
        if p.verbose ==2
            print_loop(t_vec(end),time_max,i_app(t+1),V(t+1),k,solution_time,mem)
        end
    end
    % ~~~~~~~~~~~ Compute Ageing Parameters ~~~~~~~~~~~ % 
    if p.ageing   
        % Compute Main Flux (At Negative Electrode)
        j_main(:,t) = j_tot(:,t)-j_sei(:,t)-j_lpl(:,t)-j_cath(:,t);
        
        if p.ageMech.PoreClog
        % ~~~ Pore Clogging ~~~ %

        if epse_neg_temp(:,t) > 0.001 % NOTE: Added if condition such that epse_neg does not pass below threshold to avoid break
           epse_neg_temp(:,t+1) = epse_neg_temp(:,t) - (p.dt*p.beta_pc_sei*j_sei(:,t+1)*p.F + p.dt*p.beta_pc_lpl*j_lpl(:,t+1)*p.F); % <-- Added the effect of Lithium Plating on Pore Clogging
        else
            epse_neg_temp(:,t+1) = 0.001;
        end

        end
        

        if p.ageMech.Cathode
        % ~~~ Cathode Degradation ~~~ %

        % Change in porosity due to dissolution reaction
        beta_cath = p.beta_cath;
        epss_pos_temp(:,t+1) = epss_pos_temp(:,t) + p.dt*p.beta_cath*p.F*j_cath(p.nn+1:end,t+1);

        % Lost Charge
        Closs_cath(t+1) = Closs_cath(t) + p.F*p.dt*p.dx_p*p.A_surf*(sum(abs(j_cath(1+p.nn:end,t+1).*p.a_s(1+p.nn:end))));
        else
        Closs_cath(t+1) = 0;
        end

        if p.ageMech.SurfaceCrack
        % ~~~ Surface Cracking ~~~ %
        beta_5 = p.beta_sc2; 
        beta_4 = p.beta_sc1;

        Ds_neg_crack(t) = Ds_neg*(1 - A_cr(t)/A_max)^beta_5;
        A_cr(t+1) = A_cr(t) + p.dt*beta_4*(A_max - A_cr(t))*abs(i_app(t));
        end

        if p.ageMech.LAM
        % ~~~ Loss of Active Material ~~~ %
        C_1 = p.lam_1; %2.68e-6 [Delacourt et. Al, 2012]
        C_2 = p.lam_2; %-1.3e-4

        % Change in volume fraction due to LAM
        epss_neg_temp(:,t+1) = epss_neg_temp(:,t) - p.dt*C_1*abs(j_main(1:p.nn,t)) + p.dt*C_2*sqrt(abs(j_main(1:p.nn,t))); % Eq. 5 Safari and Delacourt // Eq. 26 Reniers et al
        val1=p.dt*(C_1*abs(j_main(1:p.nn,t)));
        val2=p.dt*C_2*sqrt(abs(j_main(1:p.nn,t)));
        val3=val1+val2;
        sigma_neg(:,t) = p.sigma_neg.*epss_neg_temp(:,t);
        a_s_neg(:,t) = 3*epss_neg_temp(:,t)/p.R_neg;

        % Charge lost due to trapped lithium in LAM
        Closs_LAM(t+1) = Closs_LAM(t) - p.F*p.dt*((C_1*sum(abs(j_main(1:p.nn,t)))) + C_2*sum(sqrt(abs(j_main(1:p.nn,t)))))*soc(t)*(4/3*pi*p.R_neg^3)*p.cs_max_neg;
        else
        Closs_LAM(t+1) = 0;
        end 

        if p.ageMech.LiPlating
            % ~~~ Lithium Plating ~~~ %
            Closs_LPL(t+1) = Closs_LPL(t) + p.F*p.dt*p.dx_n*p.A_surf*(sum(abs(j_lpl(1:p.nn,t+1).*p.a_s(1:p.nn))));
        else
            Closs_LPL(t+1) = 0;
        end
        
        if p.ageMech.SEI
            % ~~~ SEI Formation ~~~ %
            Closs_SEI(t+1) = Closs_SEI(t) + p.F*p.dt*p.dx_n*p.A_surf*(sum(abs(j_sei(1:p.nn,t+1).*p.a_s(1:p.nn))));
        else
            Closs_SEI(t+1) = 0;
        end

        % ~~~ Compute Charge Lost ~~~ %   
        Cl(t+1) = Closs_SEI(t+1) + Closs_LAM(t+1) + Closs_LPL(t+1) + Closs_cath(t+1); % Combining Ageing Losses
        
        K_lpl = 0.1356; % K_lpl value to scale the effect of LPL on R_f
        Rf(:,t+1) = Rf_prevt-p.dt*p.B1.*j_sei(:,t+1)-K_lpl*p.dt*(p.V_SEI./(p.sigma_SEI).*j_lpl(:,t+1));      % Took same values as for SEI, since no values from Literature to go off of

        mem.Closs(t+1) = Cl(t+1);
    
    end
    % ~~~~~~~~~~~ Update Ageing Parameters ~~~~~~~~~~~ %
    if mod(t,500)==0 && p.ageing==1

        % ~~~ Updating New Ageing Parameters ~~~ %

        if p.ageMech.LAM
            p.epss_neg = epss_neg_temp(:,t);        % LAM affects porosity
            p.sigma_eff_neg = p.sigma_neg.*p.epss_neg;
            p.sigma_eff(1:p.nn) = p.sigma_eff_neg;  % Update Conductivity with new Epsilon
            p.a_s_neg = 3*p.epss_neg/p.R_neg;                                              
            p.a_s(1:p.nn) = p.a_s_neg;              % Update interfacial surface areaL
            p.eps_s(1:p.nn) = p.epss_neg;
        end

        if p.ageMech.PoreClog
            p.eps_e(1:p.nn) = epse_neg_temp(1:p.nn,t);     % Pore Clogging affects porosity
        end
        
        if p.ageMech.SurfaceCrack
            p.Ds_neg = Ds_neg_crack(t);          % Surface Cracking affects Diffusivity
        end

        if p.ageMech.Cathode
            p.epss_pos = epss_pos_temp(:,t);    % Cathode Degradation affects porosity
            p.sigma_eff_pos = p.sigma_pos.*p.epss_pos;
            p.sigma_eff(p.nn+1:end) = p.sigma_eff_pos;
            p.a_s_pos = 3*p.epss_pos/p.R_pos; 
            p.a_s(p.nn+1:end) = p.a_s_pos;
            p.eps_s(p.nn+1:end) = p.epss_pos;

        end

        % NEW: Update the newly time dependent parameters (p.eps_e changes with time, therefore De_eff must be updated).
        p.De_eff = @(c,T) p.De(c,T).*p.eps_e.^p.brug; 
        p.kappa_eff = @(c,T) p.kappa(c,T).*p.eps_e.^p.brug;    
        
        % NEW: Update Matrices
        m = fcn_system_matrices(p,m);

        % Compute new ageing parameters
        [Cbat_new,sbat,gamma_neg, gamma_pos] = fcn_Q(Cl(t+1),se_init,1,p); 
        p.s0_neg = sbat(3); p.s100_neg = sbat(1);
        p.s0_pos = sbat(4); p.s100_pos = sbat(2);
        se_init = sbat;
        Qs = soc_prevt*p.Cbat-(Cl(t+1)-Cl(t+1-500));
        p.Cbat = Cbat_new;
        soc_prevt = Qs/p.Cbat; 
        
        mem.Cbat_aged_current = Cbat_new;
        p.gamma_neg_prev = gamma_neg;
        p.gamma_pos_prev = gamma_pos;
        p.epss_neg_prev = p.epss_neg;
        p.epss_pos_prev = p.epss_pos;
    end
    soc(t+1) = compute_soc(cs(:,t+1),p); 
    if p.set_simp(6)
        stoich = [cs(p.nn+p.np+1:p.nn+p.np+p.nn,t+1)/p.cs_max_neg; cs(2*p.nn+p.np+1:end,t+1)/p.cs_max_pos];
    else
        stoich = [cs(p.nrn:p.nrn:p.nrn*p.nn,t+1)/p.cs_max_neg; cs(p.nrn*p.nn+p.nrp:p.nrp:end,t+1)/p.cs_max_pos];
    end
    
    if p.thermal_dynamics
        T(t+1) = fcn_T(j_tot(:,t+1), U(:,t+1),stoich,V(t+1),i_app(t+1),T_prevt, p);
    else
        T(t+1) = p.T_amb; 
    end
    solution_time = solution_time+toc();
    if (any(stoich>=1-1e-6) || any(stoich<=1e-6)) && not(max_iterations)
        end_simulation =1;
        warning('cs exceeeding either cs_max or is lower than 0. Stopping the simulation.')
        warning_set = 1;
    end
    if any(ce(:,t+1)<=1e-6) && not(max_iterations)
        end_simulation =1;
        warning_set = 1;
        warning('ce is lower than 0. Stopping the simulation.')
    end
    if (V(t+1) <p.Vmin || V(t+1) >p.Vmax) && not(max_iterations)
        warning('Voltage exceeded specified bounds. Stopping the simulation.')
        end_simulation=1;
    end
    if kl>4 
        end_simulation = 1;
    end
    if t>5 &&(t_vec(t)-t_vec(t-5))<1e-4
        end_simulation = 1;
        warning('Simulation is not progressing. Stopping the simulation. Most likely causes are: too high applied current, ce close 0, cs close to cs_max or close to 0')
    end
    if end_simulation==1
        break
    end
    if t_vec(end)>=time_max
        break
    end
    
    if max_iterations
        dt_next = 0.5*p.dt; 
        if ic>3
            dt_next = 1e-11*p.dt;
        end
        phis_prev = phis(:,t);
        kl = kl+1;
    else
        cs_prevt = cs(:,t+1); 
        ce_prevt = ce(:,t+1); 
        T_prevt = T(t+1); 
        soc_prevt = soc(t+1); 
        if p.ageing
            Cl_prevt = Cl(t+1); 
            p.i02 = p.i02f(T_prevt); 
            Rf_prevt = Rf(:,t+1); 
            p.Rf = Rf_prevt;
        end
        if isa(p.k0,'function_handle') && p.thermal_dynamics
            p.k0 = p.k0f(T_prevt); 
        end
        t = t+1;
    end
end
% ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~ % 
% ~~~~~~~~~~~ END OF SIMULATION ~~~~~~~~~~~ %
% ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~ %
out.sim_time = solution_time;

% Adding Time Skip Variable
Tskip = p.SaveDataTskip;


if warning_set==0
    if p.verbose>0
    dispstat(sprintf('Finished the simulation in %2.2f s \n',out.sim_time),'keepthis','timestamp');
    end
else
%     dispstat(sprintf('Finished the simulation in %2.2f s with warning %d \n',out.sim_time,warning_set),'keepthis','timestamp');
end
if warning_set ==1
    n_t = t+1;
else
    n_t = t+1; 
end
p.L = p.delta_neg+p.delta_sep+p.delta_pos; 

% Store states

% out.x = [p.dx_n/2*(1:2:(p.nn*2-1)) p.delta_neg+[p.dx_s/2*(1:2:(p.ns*2-1))]...
%         p.delta_neg+p.delta_sep+p.dx_p/2*(1:2:(p.np*2-1))]'/p.L; 
out.t = t_vec(2:1:n_t); 
%out.cs = cs(:,2:Tskip:n_t);
%out.ce = ce(:,2:Tskip:n_t); 
%out.phis = [phis(1:p.nn,2:Tskip:n_t); phis(p.nn+1:end,2:Tskip:n_t)]; 
%out.phie = phie(:,2:Tskip:n_t); 
% if p.set_simp(6)
%     out.cs_bar = [cs(p.nnp+(1:p.nn),2:Tskip:n_t); cs(p.nnp+(p.nn+1:p.nnp),2:Tskip:n_t)];
% else
